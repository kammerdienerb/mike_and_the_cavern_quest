typedef struct {
    int is_overworld;
    char **map;
    int num_rows, num_cols;
} level_t;

void level_make(level_t *level, char **map, int rows, int cols) {
    int i;

    level->is_overworld = 0;
    level->map          = malloc(rows * sizeof(char*));
    level->num_rows     = rows;
    level->num_cols     = cols;

    for (i = 0; i < rows; i += 1) {
        level->map[i] = strdup(map[i]);
    }
}

char level_get_at(level_t *level, int y, int x) {
    if (y < 0 || y >= level->num_rows) {
        return -1;
    }
    if (x < 0 || x >= level->num_cols) {
        return -1;
    }
    return level->map[y][x];
}

void level_clear_space(level_t *level, int y, int x) {
    if (y < 0 || y >= level->num_rows) { return; }
    if (x < 0 || x >= level->num_cols) { return; }

    level->map[y][x] = ' ';
}
